def multi(x, y):
    """
    This function multiplies two numbers.

    Parameters:
    x (int or float): The first number.
    y (int or float): The second number.

    Returns:
    int or float: The product of x and y.
    """
    return x * y


def absolute(x):
    """
    This function returns the absolute value of a number.

    Parameters:
    x (int or float): The number to find the absolute value of.

    Returns:
    int or float: The absolute value of x.
    """
    return x if x >= 0 else -x


def add(x, y):
    """
    This function adds two numbers.

    Parameters:
    x (int or float): The first number.
    y (int or float): The second number.

    Returns:
    int or float: The sum of x and y.
    """
    return x + y


def subtract(x, y):
    """
    This function subtracts the second number from the first.

    Parameters:
    x (int or float): The first number.
    y (int or float): The second number.

    Returns:
    int or float: The result of x - y.
    """
    return x - y


def double(x):
    """
    This function doubles a number.

    Parameters:
    x (int or float): The number to double.

    Returns:
    int or float: The doubled value of x.
    """
    return x * 2
